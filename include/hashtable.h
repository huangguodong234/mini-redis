#ifndef HASHTABLE_H
#define HASHTABLE_H

/* ============================================================
 * mini-redis 对照 Redis 8.0.6 源码阅读笔记
 * 模块：哈希表 (dict)
 * ============================================================
 */

/* ============================================================
 * 一、key 存储方式的设计权衡
 * ============================================================
 *
 * 我的 mini-redis：用 char * 固定死，只能处理原生 C 字符串，无法记录长度等信息。
 *
 * Redis：底层 dictEntry 的 key 定义为 void *，这是为了把 dict 打造成通用工具
 *         （通过 dictType 支持内部多种映射场景）。
 *         但在具体的主数据库业务中，Key 的类型是受限的（只能是字符串）。
 *         当收到 TCP 字节流并解析出参数后，Redis 会调用 malloc 将其包装成
 *         SDS 结构体，然后将 SDS 的指针存入 dictEntry 的 void *key 中。
 *
 * 这两种方式的区别是：
 *
 * 1. 数据存储的基础形态：
 *    我的 mini-redis：直接使用原生的 char * 存储键和值。
 *    Redis：将字符串包装成 SDS 结构体。因为 SDS 是"二进制安全"的，
 *           所以可以存储图片、视频等任何包含 \0 的二进制数据流。
 *
 * 2. 为什么 Redis 要把数据包装成 SDS 结构体？
 *
 *    (1) 获取长度的速度：
 *       我的 mini-redis：原生 C 字符串，通过 \0 判断终止，从头遍历，时间复杂度 O(n)。
 *       Redis：SDS 结构体内部包含 len 字段，获取长度时间复杂度 O(1)。
 *
 *    (2) 二进制安全：
 *       我的 mini-redis：依赖 \0 标记结束，处理含 \0 的二进制数据（如图片）时会被截断。
 *       Redis：依赖 len 判断长度，无视 \0，保证数据完整。
 *
 *    (3) 内存溢出风险：
 *       我的 mini-redis：拼接字符串时若空间不足可能导致程序崩溃（需手动管理动态扩容）。
 *       Redis：SDS API 拼接前自动检查 len 和 alloc，不足时自动 realloc，杜绝溢出。
 *
 *    (4) 频繁修改时的内存分配次数：
 *       我的 mini-redis：不记录总容量，每次加长必须 realloc，每次缩短必须 free。
 *                     （修改次数与内存分配/释放 1:1 绑定，系统调用开销大）
 *
 *       Redis：SDS 记录了 len（已用）和 alloc（总分配），采用两大策略优化：
 *             1. 空间预分配（减少 malloc）：新长度 < 1MB 按 2 倍申请；
 *                >= 1MB 固定多分配 1MB。下次追加直接写空闲区。
 *             2. 惰性空间释放（减少 free）：缩短时不立即 free，标记为空闲。
 *                下次变长时复用。
 *
 *       结论：Redis 通过预分配和惰性释放，大幅降低了高频写场景下的
 *             malloc/free 系统调用频率。
 */

/* ============================================================
 * 二、value 存储方式的设计权衡
 * ============================================================
 *
 * 1. 从内存分配开销来看：
 *    我的实现：char *value 是指针类型。先要申请一次节点的内存，存数据时
 *            用 strdup，strdup 会调用 malloc 又是一次内核态切换。
 *            实现一次插入要两次内存分配的开销。
 *
 *    Redis 的实现：union 共用体类型。申请一次 dictEntry 结构体的内存时
 *               把 union 的内存一起申请了。只有一次内存分配的开销。
 *
 * 2. 从避免重复内存分配来看：
 *    我的实现：申请了两次内存，增加了内存管理的负担。
 *    Redis 的实现：只申请了一块内存，既解决了节点本身的需要，也为小的
 *                整数值准备好了"车位"。
 *
 * 3. 从消除不必要的类型转换来看：
 *    我的实现：如果进来的数据不是字符串类型，还要再申请一块内存，
 *            来做类型转换后的存储。
 *    Redis 的实现：不管是什么类型的都可以存，不用做类型转换。
 *                因为 union 可以设置所有的类型。
 */


/* ============================================================
 * 三、dictType / dictEntry / dict 三层架构
 * ============================================================
 *
 * 之后通过 dictType 函数告诉调用者，怎么处理数据，包括怎么算哈希，
 * 怎么复制 key/value，怎么比大小，键值不用了怎么销毁，什么时候扩容
 * 哈希表，什么时候清理旧的哈希表，申请的空间没用完时怎么办。
 *
 * dictType   — 接口，是操作手册（C 语言实现多态的经典手法——函数指针表）
 * dictEntry  — 数据节点（Data Object），只存数据，没有任何行为
 * dict       — 持有数据节点 + 持有接口，把两者粘在一起
 *
 * 关于 dictType（字典的定制说明书）：
 *   把"怎么处理数据"从核心逻辑中抽离，做成配置项，方便后续优化。
 *
 *   动态调用：只存函数指针（哈希、比较、销毁等）。核心代码写死一套，
 *            传入不同的 dictType，就能动态调用对应数据类型（如 SDS、
 *            整数）的处理函数。
 *
 *   扩容控制：提供是否允许扩容、搬家前后的回调钩子，让底层扩容过程可控。
 */


/* ============================================================
 * 四、扩容策略的区别
 * ============================================================
 *
 * 我的 mini-redis：扩容时一次性把旧表数据全部搬到新表。
 *
 * Redis：渐进式扩容（rehash），在每次添加/删除/查找时只搬一个桶。
 *
 * 为什么 Redis 不用一次性搬迁？
 *   - 当数据量很大时（千万级 key），一次性搬迁耗时几百毫秒甚至秒级，
 *     这期间 Redis 无法处理任何请求，造成严重延迟。
 *   - 渐进式搬迁把耗时摊平到每次操作里，单次搬迁微秒级，客户端无感。
 *
 * 搬迁策略的细节：
 *   - 每次 dictAdd/dictFind/dictDelete 前顺带搬一个桶
 *     （_dictRehashStepIfNeeded）。
 *   - empty_visits 上限是限制跳过空桶的次数，防止在稀疏表上空转太久。
 *   - 搬桶逻辑在 rehashEntriesInBucketAtIndex 里，内部通过 no_value 标志
 *     区分有 value 和无 value 两种搬迁路径，不是两套独立函数。
 *
 * 扩容时机控制（dict_can_resize 全局变量）：
 *   - DICT_RESIZE_ENABLE：正常状态，负载因子 >= 1 即扩容。
 *   - DICT_RESIZE_AVOID：有子进程持久化时，负载因子 >= 4 才扩容，
 *     避免 fork 后的写时复制导致内存暴涨。
 *   - DICT_RESIZE_FORBID：完全禁止 rehash。
 */


/* ============================================================
 * 五、插入/查找/删除操作的区别
 * ============================================================
 *
 * 插入 key 操作的区别：
 *   我：只有一张哈希表，直接插入。
 *   Redis：有两张表。rehash 期间新 key 直接插入新表（ht_table[1]），
 *   这样旧表只减不增，保证最终能搬空。如果插入旧表，搬迁时又要多搬
 *   一个节点。
 *
 * 查找/删除操作的区别：
 *   我：只查一张表。
 *   Redis：rehash 期间必须遍历两张表，因为数据可能还在旧表没搬完。
 *   遍历旧表时，索引小于 rehashidx 的桶已被搬迁，可以直接跳过。
 */


/* ============================================================
 * 六、极致省内存的设计
 * ============================================================
 *
 * Redis 有三层存储策略（从省到费）：
 *   1. 空桶直存：key 指针直接塞进桶位，连 entry 都不分配
 *      （通过指针低位标记区分类型）
 *   2. 轻量节点：碰撞时分配 dictEntryNoValue，只有 key + next，
 *      省掉 union v（8 字节）
 *   3. 完整节点：普通字典模式，分配完整 dictEntry
 *
 * 策略选择由 dictType 中的位域标志控制（no_value、keys_are_odd），
 * 只占 1 bit。成千上万个同类字典实例共享同一个 dictType，标志位
 * 只需存一份。这是类级别的内存优化——把元数据从"每个实例存一份"
 * 提升到"每种类型存一份"。
 *
 * 通过指针低 3 位标记（ENTRY_PTR_MASK）区分桶里存的是哪种：
 *   000 → 普通 dictEntry
 *   001 → 奇数地址的 key 指针
 *   010 → 偶数地址的 key 指针
 *   100 → dictEntryNoValue 节点
 *
 * 注意：8.0 版本将 dictEntryNoValue 从 dict.h 移入 dict.c，
 * 变成私有实现细节。外部只需设置 dictType.no_value = 1，
 * 内部自动选择最优存储。
 */

// 哈希表节点（链表节点）
typedef struct HashNode{
    char *key;
    char *value;
    struct HashNode *next;    // 指向下一个节点
}HashNode;

//哈希表结构体
typedef struct{
    HashNode **buckets;  // 桶数组（指针数组）
    int size;            // 当前存储的键值对个数
    int capacity;         // 桶的数量
}HashTable;

// djb2 哈希函数
unsigned long hash_djb2(const char *str);

// 创建哈希表，initial_capacity 是初始桶数
HashTable *hashtable_create(int initial_capacity);

//插入或更新键值对-SET命令
void hashtable_set(HashTable *ht,const char*key,const char *value);

//查找 key，返回 value 指针（找不到返回 NULL）-GET命令
char *hashtable_get(HashTable *ht,const char *key);

// 删除 key，返回 1 成功，0 不存在-DEL命令
int hashtable_del(HashTable *ht,const char *key);

//销毁哈希表，释放所有内存
void hashtable_free(HashTable*ht);

#endif
